package javaFainal.data;

public class Variable_변수 {
	public static void main(String[] args) {
		int x = 10; // 변수 선언
		System.out.println(15); // 숫자표기
		System.out.println(34 + 10); // 수식표기
		System.out.println(x + 30); // 변수 수식표기

		x = 77; // 변수 초기화(미리 값넣은거 변경)
		System.out.println(x); // 초기화한 변수 표기
	}
}
