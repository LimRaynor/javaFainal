package javaFainal.data;

public class DataType_Litera_데이터타입_리터럴 {
	public static void main(String[] args) {

		// 정수형 (Integer)
		byte b = 1;
		short s = 2;
		int x = 4;
		long l = 8L;

		// 실수형 (Floating Point)
		float ff = 4.0f;
		double dd = 8.0;

		// 논리 자료형 (boolean)
		boolean isMarried = true;
		isMarried = false;

		// 문자형 (Character)
		char c = 'a';
		char cc = '한';

		// 문자열 String
		String str = "여러 글자";

		System.out.println(x);

	}
}
